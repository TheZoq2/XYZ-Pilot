SLOW_TURN = 0000000000000000


#Modify the model memory
ASTEROID_START = 7A
LOAD 0 ASTEROID_START
STOREOBJ 0 7

ITER:
  LOAD 0 SHIPPOS
  LOAD 1 SHIPVEL
  LOAD 3 VECADDRIGHT
  LOAD 4 VECADDDOWN
  LOAD 6 VECROTRIGHT

  LOAD 7 START_ANGLE
  LOAD 8 TURN_SPEED

  LOAD 2 SLOW_TURN_ADD
  LOAD 5 SLOW_TURN

  # STYR SKEPPETS HASTIGHET
  TESTLEFT:
    BTST F 5
    BEQ GOLEFT
  TESTDOWN:
    BTST F 4
    BEQ GODOWN
  TESTUP:
    BTST F 6
    BEQ GOUP
  TESTRIGHT:
    BTST F 3
    BEQ GORIGHT
  TEST_TURN_LEFT:
    BTST F 0
    BEQ TURN_LEFT
  TEST_TURN_RIGHT:
    BTST F 1
    BEQ TURN_RIGHT
  
  BRA MOVED


  MOVED:

  #Update ship position and angle
  ADD 0 0 1
  STOREOBJ 0 0
  STOREOBJ 7 1

  VECADD 5 5 2
  STOREOBJ 5 5

  STORE 0 SHIPPOS
  STORE 1 SHIPVEL
  STORE 3 VECADDRIGHT
  STORE 4 VECADDDOWN
  STORE 5 VECROTRIGHT
  STORE 7 START_ANGLE
  STORE 8 TURN_SPEED

  STORE 2 SLOW_TURN_ADD
  STORE 5 SLOW_TURN
  #Update the angle of the ship

  WAITFRAME
  WAITFRAME

  BRA ITER


  
#Movement 'subroutines'
GORIGHT:
  ADD 1 1 3
  BRA TEST_TURN_LEFT
GOLEFT:
  SUB 1 1 3
  BRA TESTDOWN
GODOWN:
  ADD 1 1 4
  BRA TESTUP
GOUP:
  SUB 1 1 4
  BRA TESTRIGHT
TURN_LEFT:
  SUB 7 7 8
  BRA TEST_TURN_RIGHT
TURN_RIGHT:
  ADD 7 7 8
  BRA MOVED
